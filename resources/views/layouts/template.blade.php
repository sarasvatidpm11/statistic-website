                <p class="text-justify">
                    Statistics is the study of how to plan, collect, analyze, interpret and display data. In short, statistics is a science that deals with data. The term 'statistics' (English: stats) is different from 'statistics' (statistics). Statistics is the science that deals with data, while statistics is data, information, or the result of applying statistical algorithms to data. From a collection of data, statistics can be used to conclude or describe data; these are called descriptive statistics. Most of the basic concepts of statistical probability theory assumes. Some statistical terms include: population, sample, sample unit, and probability. <br> <br>

                    Statistics is widely applied in various scientific disciplines, both natural sciences (eg astronomy and biology as well as social sciences (including sociology and psychology), as well as in business, economics and industry. Statistics is also used in government for various purposes; Population census is one of the most well-known procedures. Other applications of statistics that are now popular are polling or polling procedures (for example, carried out before a general election), as well as quick counts (quick calculation of election results) or quick counts.In the field of calculations, statistics can also applied in pattern recognition and artificial intelligence. <br> <br>

                    The use of the term statistics was removed from the modern Latin statisticum collegium ("council of state") and the Italian statista ("statesman" or "politician"). Gottfried Achenwall (1749) used Statistics in German for the first time as a name for the activity of analyzing state data, meaning "the study of states (states)". At the beginning of the 19th century there has been a shift in meaning to "the science of collecting and classifying data". Sir John Sinclair introduced the name (Statistics) and this meaning into English. So, statistics, in principle, initially only dealt with data used by administrative and governmental institutions. Data collection is ongoing, especially through regular censuses to provide population information that changes over time. <br> <br>

                    In the 19th century and early 20th century statistics began to make extensive use of fields in mathematics, especially probability. The branch of statistics that is currently very widely used to support the scientific method, statistical inference, was developed in the second half of the 19th and early 20th centuries by [Ronald Fisher] (founder of statistical inference), Karl Pearson (linear regression method) , and William Sealey Gosset (researching the problem of small samples). Today's use of statistics can be said to have touched all fields of science, from astronomy to linguistics. The fields of economics, biology and its applied branches, as well as psychology are heavily influenced by statistics in their methodology. As a result, combined sciences such as econometrics, biometrics (or biostatistics) and psychometrics were born.<br> <br>

                    Although there are those who consider statistics as mathematicians, others consider statistics as a field that is closely related to mathematics, looking at its history and applications. In Indonesia, most statistical studies are included in the faculties of mathematics and natural sciences, both in separate departments and in association with mathematics.
